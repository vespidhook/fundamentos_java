public class ControleDeFluxo {
    
    public static void main(String[] args) {
        // Condicionais

        int numero = 11;
        
        if(numero == 10) {
            System.out.println("Número igual a 10");
        } else if (numero == 11) {
            System.out.println("Número igual a 11");
        } else {
            System.out.println("Número não é igual a 10 nem a 11");
        }

        if(numero > 1) {
            System.out.println("Número maior que 1");
        }

        if(numero > 2) {
            System.out.println("Número maior que 2");
        }
    }
}
